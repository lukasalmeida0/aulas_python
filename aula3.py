# Aspas simples
print('Lukas Almeida')
print(1, 'Lukas "Almeida"')

# Aspas duplas
print("Lukas Almeida")
print(2, "Lukas 'Almeida'")

# Escape
print('escape', "Lukas \"Almeida\"") #escape é para pular o próximo caractere 

# r
print(r'r"Lukas \"Almeida\""')#r no começo de print exibe tudo dentro do parenteses

# Ou pode ser feito "" dentro de '' para diferenciar e o codigo ficar mais limpo como no exemplo abaixo
print('"Lukas Almeida"')